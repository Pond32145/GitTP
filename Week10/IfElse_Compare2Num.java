package Week10;

public class IfElse_Compare2Num {
    public static void main(String[] args) {
        int a=4,b=3;
      
        if (a>b) {
            System.out.println(a+" มากกว่า"+b);
        }else if(a<b){
            System.out.println(a+"น้อยกว่า"+b);
        }else{
            System.out.println(a+"เท่ากับ"+b);
        }
    }
}
