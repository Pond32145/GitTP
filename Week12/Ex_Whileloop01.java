package Week12;

public class Ex_Whileloop01 {
    public static void main(String[] args) {
        int i = 1;
        while(i<=10) {
            System.out.print(i+" ");
           i++;
        } System.out.println();
        
        for (int j =1; j >=1; j--) {
            System.out.print(i+" ");
        }
    }
}